/// مساعدات المقاعد — نقل حرفي من `reference/src/engine.js`.
///
/// اللعب **عكس عقارب الساعة**: التالي هو من على اليمين = (s + 1) % 4.
/// الفريقان: المقعدان 0,2 ضد 1,3.
library;

/// المقعد التالي (يمين الحالي).
int nextSeat(int s) => (s + 1) % 4;

/// فريق المقعد: 0 → فريق 0 (مع 2) · 1 → فريق 1 (مع 3).
int teamOf(int s) => s % 2;

/// أول مضمّن = يمين الموزّع = (dealer + 1) % 4.
int firstBidder(int dealer) => nextSeat(dealer);
