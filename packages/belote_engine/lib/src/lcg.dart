/// مولّد أرقام عشوائية خطّي (LCG) — يطابق حرفياً المولّد في
/// `reference/tools/gen-fixtures.js`:
///
///   state = (state * 1664525 + 1013904223) mod 2^32
///   out   = state / 2^32   ∈ [0, 1)
///
/// الفخ في Dart: `int` هنا 64-bit، فالضرب لا يلتفّ تلقائياً عند 2^32.
/// القناع `& 0xFFFFFFFF` بعد كل خطوة **ضروري** — بدونه تنحرف كل القيم.
///
/// هذا أول ما يُترجَم ويُثبَّت. إن اختلف رقم واحد، فكل شيء بعده باطل.
class Lcg {
  int _s;

  Lcg(int seed) : _s = seed & 0xFFFFFFFF;

  double next() {
    _s = (_s * 1664525 + 1013904223) & 0xFFFFFFFF; // القناع ضروري
    return _s / 4294967296.0;
  }
}
