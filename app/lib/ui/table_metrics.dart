import 'dart:math' as math;

/// **مقاييسُ الطاولة من مقاس المسرح** — حسابٌ خالصٌ بلا ودجت، يُفحَص بالأرقام.
///
/// القاعدةُ الحاكمة: **على الهاتف لا يتغيّر شيءٌ بكسلًا واحدًا.** المسرحُ هناك
/// ضيّقٌ وعالٍ فيحكمه العرضُ كما كان دائمًا؛ وعلى نافذة حاسوبٍ (المسرحُ عندها
/// أقربُ إلى التربيع وأعلى) يدخل الارتفاعُ حاكمًا ثانيًا فتكبر الأوراقُ
/// والمقاعدُ مع النافذة بدل أن تبقى بأحجام هاتفٍ في وسط شاشةٍ كبيرة.
///
/// لماذا `min` بين حدَّي العرض والارتفاع: أيُّهما ضاق فهو القيد. ولماذا سقوفٌ
/// مطلقة: بلا سقفٍ تصير الورقةُ على شاشة 4K أكبرَ من راحة اليد.
class TableMetrics {
  TableMetrics._();

  /// عرضُ ورقةٍ في يدي — المفضَّل قبل أن تضيّقه المروحة.
  ///
  /// حدُّ العرض `w/4.9` هو ما أقرّه المالك للهاتف (4081)، ويبقى هو الحاكمَ هناك.
  static double myCardWidth(double w, double h) =>
      math.min(math.min(w / 4.9, h * 0.13), 150.0);

  /// عرضُ ظهرِ ورقةٍ في يد الخصوم.
  static double backCardWidth(double w, double h) =>
      math.min(math.min(w / 11, h * 0.055), 70.0);

  /// قطرُ **دائرة اللعب** في وسط الطاولة.
  ///
  /// كانت `min(w,h)×0.44` — وهو حسابٌ صحيحٌ على الهاتف (النسبةُ ‎≈0.46 فيحكمه
  /// العرض) **وخاطئٌ على مسرحٍ أقربَ إلى التربيع**: هناك يبقى العرضُ هو الأصغرَ
  /// فتأكل الدائرةُ ثلثَ الارتفاع وتلتصق ببطاقتي. الحدُّ من البعدين معًا: على
  /// الهاتف يكسب العرضُ (نفسُ الرقم القديم حرفيًّا)، وعلى نافذةٍ قصيرةٍ يكسب
  /// الارتفاعُ فتبقى الفرجةُ التي أقرّها المالك.
  static double wellSize(double w, double h) => math.min(w * 0.44, h * 0.29);

  /// قطرُ المساحة التي تُرمى فيها أوراقُ الأبلي (أوسعُ من البئر).
  static double trickSpread(double w, double h) => math.min(w * 0.66, h * 0.44);

  /// قطرُ مقعد اللاعب. أرضيّتُه 54 — مقاسُ الهاتف الذي أُقرّت عليه كلُّ نِسَب
  /// المقعد (الاسمُ · الرتبةُ · الزرّان)؛ لا ينزل عنه ولو ضاقت النافذة.
  static double seatSize(double w, double h) =>
      math.min(w / 6.5, h * 0.06).clamp(54.0, 78.0);
}
